<template>
  <div class="list-group-item">
    <div v-if="!editingTask" class="list-group-item-heading">
      <div class="d-flex w-100 justify-content-between align-items-center">
        <h2 class="mb-1 mr-auto p-2">{{task.name}}</h2>
        <div class="flex-column text-right">
          <div>
            <button @click="editTask()" class="badge badge-pill">
              <font-awesome-icon icon="user-pen"/>
            </button>
            <button class="badge badge-pill">
              <font-awesome-icon icon="trash"/>
            </button>
          </div>
          <button
            v-if="task.completed"
            class="badge badge-secondary badge-pill"
            @click="toggleTaskCompletion()"
          >
            Done
          </button>
          <button
            v-else
            class="badge badge-warning badge-pill"
            @click="toggleTaskCompletion()"
          >
            In Progress
          </button>
        </div>
      </div>
      <div class="align-items-end d-flex w-100 justify-content-between">
        <p class="p-2 my-2 list-group-item-light list-group-item-secondary">
          {{task.description}}
        </p>
        <AppDate :timestamp="task.updated" class="badge badge-light badge-pill pull-right"/>
      </div>
    </div>
    <div v-else>
      <TaskEditor
        :task="task"
        @finishEditingTask="finishEditingTask"
        @saveEditedTask="saveEditedTask"
      />
    </div>
    <div class="pt-3 container list-group">
      <SubTaskList
        v-if="activeTaskSubTasks.length != 0"
        :subTasks="activeTaskSubTasks"
        @saveEditedSubTask="saveEditedSubTask"
        @updateSubTaskCompletion="updateSubTaskCompletion"
      />
      <SubTaskAdd @addSubTask="addSubTask"/>
    </div>
  </div>
</template> 

<script>
import sourceData from '@/data.json'
import SubTaskList from '@/components/SubTaskList.vue'
import SubTaskAdd from '@/components/SubTaskAdd.vue'
import TaskEditor from '@/components/TaskEditor.vue'
export default {
  components: {
    SubTaskList,
    SubTaskAdd,
    TaskEditor
  },
  data () {
    return {
      tasks: sourceData.tasks,
      subTasks: sourceData.subTasks,
      editingTask: false
    }
  },
  props: {
    taskId: {
      required: true,
      type: String
    },
    goalId: {
      required: true,
      type: String
    }
  },
  computed: {
    task () {
      return this.tasks.find(task => task.id === parseInt(this.taskId))
    },
    taskSubTasks () {
      return this.subTasks.filter(subTask => subTask.task === parseInt(this.taskId))
    },
    activeTaskSubTasks () {
      return this.taskSubTasks.filter(subTask => subTask.removed === false)
    }
  },
  methods: {
    editTask () {
      return this.editingTask = true
    },
    finishEditingTask () {
      return this.editingTask = false
    },
    saveEditedTask (eventData) {
      const editedTask = {
        ...eventData.editedTask,
        updated: new Date(Date.now()).toISOString()
      }
      this.tasks[
        this.tasks.indexOf(
          this.tasks.find(task => task.id === editedTask.id)
        )
      ] = editedTask
    },
    addSubTask (eventData) {
      const newSubTask = {
        ...eventData.newSubTask,
        completed: false,
        id: this.subTasks[this.subTasks.length - 1].id + 1,
        goal: parseInt(this.goalId),
        task: parseInt(this.taskId),
        user: this.task.user,
        removed: false
      }
      this.subTasks.push(newSubTask)
    },
    saveEditedSubTask (eventData) {
      const editedSubTask = {
        ...eventData.editedSubTask,
        updated: new Date(Date.now()).toISOString()
      }
      this.subTasks[
        this.subTasks.indexOf(
          this.subTasks.find(subTask => subTask.id === editedSubTask.id)
        )
      ] = editedSubTask
    },
    toggleTaskCompletion() {
      this.task.completed = !this.task.completed
    },
    updateSubTaskCompletion (eventData) {
      const updatedSubTask = {
        ...eventData.updatedSubTask
      }
      this.subTasks[
        this.subTasks.indexOf(
          this.subTasks.find(subTask => subTask.id === updatedSubTask.id)
        )
      ] = updatedSubTask
    }
  }
}
</script>

